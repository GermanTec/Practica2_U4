    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try{
            if (idGlobal.isEmpty()) {
                mensaje("IDVacio");
                return;
            }
        Document filtro=new Document("_id", new ObjectId(idGlobal));    
        Document dataActualizar=new Document();
        
        dataActualizar.put("nombre", jTextField1.getText());
        dataActualizar.put("domicilio", jTextField2.getText());
        dataActualizar.put("edad", Integer.parseInt(jTextField3.getText()));
        dataActualizar.put("sueldo", Double.parseDouble(jTextField4.getText()));
        
        UpdateResult respuesta=coleccion.updateOne(filtro,new Document("$set", dataActualizar));
        
            if (respuesta.getMatchedCount()==1) {
                mensaje("Se actualizo datos");
                cargarDatos();
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
            }else{
            mensaje("ERROR!!\n No se actualizo nada");
            }
            
        }catch(Exception e){
        mensaje(e.getMessage());
        }
        jButton1.setEnabled(true);
        jButton4.setEnabled(false);
    }//GEN-LAST:event_jButton4ActionPerformed
    
    private boolean eliminar(String id){
    try{
    Document eliminar=new Document("_id",new ObjectId(id));
    DeleteResult resultado=coleccion.deleteOne(eliminar);
        if (resultado.getDeletedCount()==1) {
            return true;
        }else{
            return false;
        }
    }catch(Exception e){
    mensaje(e.getMessage());
    }
    return false;
    }
    
    private boolean estaSeleccionado(){
    if(jTable1.getSelectedRow()>=0){
    return true;
    }
    return false;
    }
    
    public void cargarDatos(){
    try{
    MongoCursor<Document> documentos=coleccion.find().iterator();
    if(tablin.getRowCount()!=0){
    int total=tablin.getRowCount();
        for (int i = 1; i <=total; i++) {
            tablin.removeRow(0);
            
        }
    }
    while(documentos.hasNext()){
    String[] ren=new String[5];
    ArrayList<Object> doc=new ArrayList(documentos.next().values());
    ren[0]=doc.get(0).toString();
    ren[1]=doc.get(1).toString();
    ren[2]=doc.get(2).toString();
    ren[3]=doc.get(3).toString();
    ren[4]=doc.get(4).toString();
    tablin.addRow(ren);
    }
    }catch(Exception e){
    mensaje(e.getMessage());
    }
    }
