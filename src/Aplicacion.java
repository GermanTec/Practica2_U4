    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (estaSeleccionado()==false) {
            mensaje("Error/Debes seleccionar la fila a borrar");
            return;
        }
        int fila=jTable1.getSelectedRow();
        idGlobal=tablin.getValueAt(fila, 0).toString();
        int res=JOptionPane.showConfirmDialog(this, "SEGURO QUE DESEAS BORRAR LA FILA SELECCIONADA"+idGlobal+"?");
        if (eliminar(idGlobal)==true) {
            mensaje("Se elimino Correctamente");
            cargarDatos();
        }else{
            mensaje("No se pudo eliminar"+idGlobal);
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed

